Офисный комплекс расположен на первой линии Краснопролетарской улицы, в пяти
минутах пешком от метро «Новослободская». На территории комплекса «Новослободский» можно снять офис от 10 до
1000&nbsp;кв.&nbsp;м с высокими потолками (от 3&nbsp;м) и увеличенными окнами. Сдаются отдельные кабинеты,
офисные блоки различного размера и состава или open-space площади с возможностью установок легких
перегородок.